import { DatabaseSync } from 'node:sqlite';
const db = new DatabaseSync('cortex/bus.sqlite');
db.prepare("DELETE FROM cortex_hot_memory WHERE fact_text LIKE '%WhatsApp channel directory%'").run();
db.prepare("DELETE FROM cortex_hot_memory WHERE fact_text LIKE '%Week 8 included%'").run();
db.prepare("DELETE FROM cortex_hot_memory WHERE fact_text LIKE '%Workspace root contains%'").run();
db.prepare("DELETE FROM cortex_hot_memory WHERE fact_text LIKE '%7,300 lines%'").run();
const c = db.prepare('SELECT COUNT(*) as c FROM cortex_hot_memory').get();
console.log('Final count:', c.c);
const facts = db.prepare('SELECT fact_text FROM cortex_hot_memory ORDER BY created_at').all();
for (const f of facts) console.log(`  • ${f.fact_text}`);
db.close();
